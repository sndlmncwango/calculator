function add(value1,value2){
    sum = value1 += value2
    return sum
}
function subtract(value1,value2){
    sum = value1 -= value2
    return sum
}
function divide(value1,value2){
    sum = value1 /= value2
    return sum
}
function multiply(value1,value2){
    sum = value1 *= value2
    return sum
}
